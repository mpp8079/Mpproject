
public class oddAndEven {
	
		
		public static void main(String[]args){
			
			for(int i = 0;i <=10; i ++){
				if (i %2 ==0)
					System.out.println(i  +" is the even number");
				else 
					System.out.println(i + " is the odd number");
			}
			
			
		}
		
	}



		
		
		
		
		
		
		


